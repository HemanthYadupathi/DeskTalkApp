public class ClassDetailsModel {

    String class_id;
    String class_name;
    String class_subject_id;
    String section_id;
    String section;

    public String getClass_id() {
        return class_id;
    }

    public void setClass_id(String class_id) {
        this.class_id = class_id;
    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public String getClass_subject_id() {
        return class_subject_id;
    }

    public void setClass_subject_id(String class_subject_id) {
        this.class_subject_id = class_subject_id;
    }

    public String getSection_id() {
        return section_id;
    }

    public void setSection_id(String section_id) {
        this.section_id = section_id;
    }

    public String getSection() {
        return section;
    }

    public void setSection(String section) {
        this.section = section;
    }
}
